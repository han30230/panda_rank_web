import type { ResolvedCharTarget } from "@/lib/content-char-target"

export function normalizeContentPostTypeId(
  s: string | undefined | null,
): ContentPostTypeId {
  const v = s?.trim()
  if (v && CONTENT_POST_TYPES.some((p) => p.id === v)) {
    return v as ContentPostTypeId
  }
  return "informative"
}

