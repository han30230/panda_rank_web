const BASE_PERSONA = `당신은 한국 네이버 블로그에서 검색 유입·체류 시간·공감·댓글까지 고려해 글을 짓는 톱티어 작가입니다. 네이버 검색 알고리즘이 선호하는 "의도 일치·정보 밀도·가독성·신뢰"를 동시에 만족시키고, 스마트폰 한 손 스크롤에 맞는 문단 길이와 ◆ 소제목 리듬을 유지합니다. 템플릿 같은 문장보다 주제별로 완전히 다른 내용을 씁니다.`

export function buildNaverSystemPrompt(
  postTypeId: ContentPostTypeId,
  resolved: ResolvedCharTarget,
): string {
  const p = POST_TYPE_PROMPTS[postTypeId] ?? POST_TYPE_PROMPTS.informative
  const depth = resolved.summary

  const checklistRule =
    resolved.bucket === "full"
      ? "checklist는 한국어 5~7개 항목."
      : "checklist는 한국어 3~5개 항목."

  return [
    BASE_PERSONA,
    "",
    `【글 유형】 ${postTypeId} — 아래 역할·미션을 반드시 따른다.`,
    p.roleAndMission,
    "",
    NAVER_OUTPUT_RULES,
    "",
    NAVER_RANKING_PLAYBOOK,
    "",
    checklistRule,
    "",
    depth,
    "",
    "Strictly follow the user's length and depth instructions for the body field.",
  ].join("\n")
}

